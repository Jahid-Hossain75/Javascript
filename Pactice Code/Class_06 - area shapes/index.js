var base, height, area, a, b, r;
// rectangle & square
base = 5;
height = 6;

area = base * height;
document.write("Rectangle & Square: " + area + "<br><br>");




// triangle
base = 5;
height = 6;

area = (base * height) / 2;
document.write("Triangle are: " + area + "<br><br>");




// trapezium
a = 10;
b = 10;
height = 4;

area = ((a + b) * height) / 2;
document.write("Trapezium: " + area + "<br><br>");



// circle
r = 5;

area = 3.1415 * (r * r);
document.write("Circle: " + area);
