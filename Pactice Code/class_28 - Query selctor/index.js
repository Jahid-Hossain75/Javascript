
// using querySelector
// querySelector id = using #
document.querySelector("#heading").innerHTML = "First Heading";
// querySelector class = using .
document.querySelector(".heading2").innerHTML = "Second Heading";
// querySelector tag name = example p
document.querySelector("p").innerHTML = "Third Heading";




document.querySelector("a").innerHTML = "About";
document.querySelector("li a").innerHTML = "my link";
document.querySelector(".my_div a").innerHTML = "Blog";


